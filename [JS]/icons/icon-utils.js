// Function to render icon in DOM
function renderIcon(container, name, className = '', size = 24) {
  const iconElement = createIcon(name, className, size);
  container.appendChild(iconElement);
  return iconElement;
}

// Function to register a custom icon
function registerIcon(name, svgString) {
  if (!window.customIcons) {
    window.customIcons = {};
  }
  window.customIcons[name] = svgString;
}

// Function to get a custom icon
function getCustomIcon(name, size) {
  if (window.customIcons && window.customIcons[name]) {
    return window.customIcons[name].replace(/width="[^"]*"/, `width="${size}"`).replace(/height="[^"]*"/, `height="${size}"`);
  }
  return '';
}

